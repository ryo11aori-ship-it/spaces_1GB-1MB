import sys

# Stage 7 (Linux): ELF Binary Generator
# Generates a Spaces program that outputs a minimal x86_64 ELF binary.
# Exit status: 42

def main():
    bf = []
    
    def emit_byte(val):
        if val == 0:
            bf.append('.')
        else:
            bf.append('+' * val)
            bf.append('.')
            bf.append('[-]')

    # Minimal x86-64 ELF header
    # Returns 42 (mov al, 60; mov dil, 42; syscall)
    
    elf_bytes = [
        # ELF Header
        0x7f, 0x45, 0x4c, 0x46, 0x02, 0x01, 0x01, 0x00, 
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x02, 0x00, 0x3e, 0x00, 0x01, 0x00, 0x00, 0x00,
        0x78, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x40, 0x00, 0x38, 0x00,
        0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        
        # Program Header
        0x01, 0x00, 0x00, 0x00, 0x05, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x89, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, # FileSize (137)
        0x89, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, # MemSize
        0x00, 0x00, 0x20, 0x00, 0x00, 0x00, 0x00, 0x00,
        
        # Code (starts at offset 0x78)
        # mov al, 60
        0xb0, 0x3c,
        # mov dil, 42
        0x40, 0xb7, 0x2a, 
        # syscall
        0x0f, 0x05
    ]

    for b in elf_bytes:
        emit_byte(b)

    # Output as Spaces (UTF-8)
    S, F = " ", "\u3000"
    mapping = {'>':S*3, '<':S*2+F, '+':S+F+S, '-':S+F+F, '.':F+S+S, ',':F+S+F, '[':F*2+S, ']':F*3}
    full_bf = "".join(bf)
    
    # Secure binary write for stdout
    output_str = "".join([mapping.get(c, '') for c in full_bf])
    sys.stdout.buffer.write(output_str.encode('utf-8'))

if __name__ == "__main__":
    main()
