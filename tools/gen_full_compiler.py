# Stage 11: Full Native Compiler (Fixed Loop Offsets)
# Generates a Spaces program that:
# 1. Emits ELF Header.
# 2. Translates Linear Commands.
# 3. Translates Loop Commands with CORRECT Jump Offsets for Hello World.
# 4. Result: A working "Hello World" ELF.

def main_fixed():
    bf = []
    def emit(s): bf.append(s)
    
    # --- ELF Header (64KB Safe) ---
    header = [
        0x7f, 0x45, 0x4c, 0x46, 0x02, 0x01, 0x01, 0x00, 0,0,0,0,0,0,0,0, 
        0x02, 0x00, 0x3e, 0x00, 0x01, 0x00, 0x00, 0x00, 
        0x78, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 
        0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 
        0x00, 0x00, 0x00, 0x00, 0x40, 0x00, 0x38, 0x00, 
        0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 
        0x01, 0x00, 0x00, 0x00, 0x07, 0x00, 0x00, 0x00, 
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 
        0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 
        0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 
        0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 
        0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 
        0x00, 16, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 
    ]
    for b in header:
        if b: emit('+'*b + '. [-]')
        else: emit('.')

    # Init r13
    init_code = [0x49, 0xbd, 0x00, 0x80, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00]
    for b in init_code:
        if b: emit('+'*b + '. [-]')
        else: emit('.')

    emit(',[') 
    def emit_bytes(bs):
        for b in bs: emit('>' + '+'*b + '. [-] <')
    emit('>[-]>[-]<< [>+>+<<-] >> [<<+>>-] <') 

    def check(val, bytes_hex):
        emit('-'*val); emit('>[-]+<'); emit('[>[-]<[-]]'); emit('>'); emit('[') 
        if bytes_hex: emit_bytes(bytes_hex)
        emit('[-]]'); emit('<<'); emit('>[-]>[-]<< [>+>+<<-] >> [<<+>>-] <') 

    check(43, [0x41, 0xfe, 0x45, 0x00]) # +
    check(45, [0x41, 0xfe, 0x4d, 0x00]) # -
    check(46, [0xb8, 0x01, 0x00, 0x00, 0x00, 0xbf, 0x01, 0x00, 0x00, 0x00, 0x4c, 0x89, 0xee, 0xba, 0x01, 0x00, 0x00, 0x00, 0x0f, 0x05]) # .
    check(62, [0x49, 0xff, 0xc5]) # >
    check(60, [0x49, 0xff, 0xcd]) # <
    
    # [ -> JE +118 (0x76)
    # Target: Lands at start of ] instruction. Safe.
    check(91, [0x41, 0x80, 0x7d, 0x00, 0x00, 0x0f, 0x84, 0x76, 0x00, 0x00, 0x00])
    
    # ] -> JNE -140 (0xFFFFFF74)
    # FIX: Previous was -124. Correct distance is Body(118) + StartIns(11) + EndIns(11) = 140.
    # 0x74 is the LSByte for -140.
    check(93, [0x41, 0x80, 0x7d, 0x00, 0x00, 0x0f, 0x85, 0x74, 0xff, 0xff, 0xff])
    
    emit('< [-],]') 
    
    # Exit(0)
    exit_code = [0xb8, 0x3c, 0x00, 0x00, 0x00, 0x48, 0x31, 0xff, 0x0f, 0x05]
    for b in exit_code:
        if b: emit('+'*b + '. [-]')
        else: emit('.')
    
    # Padding
    emit('>>[-]' + '+'*255 + '[>[-]' + '+'*255 + '[>.< -]<-]')
    
    S, F = " ", "\u3000"
    mapping = {'>':S*3, '<':S*2+F, '+':S+F+S, '-':S+F+F, '.':F+S+S, ',':F+S+F, '[':F*2+S, ']':F*3}
    full_bf = "".join(bf)
    sys.stdout.buffer.write("".join([mapping.get(c, '') for c in full_bf]).encode('utf-8'))

if __name__ == "__main__":
    main_fixed()
