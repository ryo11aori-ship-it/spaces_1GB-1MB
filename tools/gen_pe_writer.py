import sys

# Stage 8: Windows PE Binary Generator (Cross-Compiler)
# Generates a Spaces program that outputs a valid x86_64 PE (.exe) binary.
# Target: Windows x64
# Exit status: 42 (via ret)

def main():
    bf = []
    
    def emit_byte(val):
        if val == 0:
            bf.append('.')
        else:
            bf.append('+' * val)
            bf.append('.')
            bf.append('[-]')

    # Minimal PE32+ (64-bit) Header
    # Alignment: Section=0x1000, File=0x200
    
    pe_bytes = [
        # --- MS-DOS Header ---
        0x4D, 0x5A, # 'MZ'
        0x90, 0x00, 0x03, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0x00, 0x00,
        0xB8, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 
        0x40, 0x00, 0x00, 0x00, # Pointer to PE Header

        # --- PE Header ---
        0x50, 0x45, 0x00, 0x00, # 'PE\0\0'
        0x64, 0x86, # Machine (x64)
        0x01, 0x00, # Number of Sections (1)
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00,
        0xF0, 0x00, # SizeOfOptionalHeader
        0x2E, 0x00, # Characteristics (Executable | LargeAddressAware)

        # --- Optional Header ---
        0x0B, 0x02, # Magic (PE32+)
        0x02, 0x1E, # Linker Version
        0x00, 0x02, 0x00, 0x00, # SizeOfCode (512 bytes)
        0x00, 0x02, 0x00, 0x00, # SizeOfInitializedData
        0x00, 0x00, 0x00, 0x00,
        0x00, 0x10, 0x00, 0x00, # AddressOfEntryPoint (RVA 0x1000)
        0x00, 0x10, 0x00, 0x00, # BaseOfCode

        # --- Windows Specific ---
        0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, # ImageBase (0x400000)
        0x04, 0x00, 0x00, 0x00, # SectionAlignment (4096)
        0x00, 0x02, 0x00, 0x00, # FileAlignment (512)
        0x06, 0x00, 0x00, 0x00, # MajorOSVersion (6.0 for compat)
        0x00, 0x00, 0x00, 0x00,
        0x06, 0x00, 0x00, 0x00, # MajorSubsystemVersion
        0x00, 0x00, 0x00, 0x00,
        0x00, 0x20, 0x00, 0x00, # SizeOfImage (0x2000 = Header + 1 Section)
        0x00, 0x02, 0x00, 0x00, # SizeOfHeaders (512)
        0x00, 0x00, 0x00, 0x00, # CheckSum
        0x03, 0x00, # Subsystem (Console)
        0x00, 0x00, # DllCharacteristics
        0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, # StackReserve
        0x00, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, # StackCommit
        0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, # HeapReserve
        0x00, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, # HeapCommit
        0x00, 0x00, 0x00, 0x00, # LoaderFlags
        0x10, 0x00, 0x00, 0x00, # NumberOfRvaAndSizes

        # --- Data Directories (Zero) ---
    ]
    pe_bytes.extend([0] * 128)

    # --- Section Table (.text) ---
    section_bytes = [
        0x2E, 0x74, 0x65, 0x78, 0x74, 0x00, 0x00, 0x00, # Name ".text"
        0x06, 0x00, 0x00, 0x00, # VirtualSize (Actual code size)
        0x00, 0x10, 0x00, 0x00, # VirtualAddress (RVA 0x1000)
        
        # FIX: SizeOfRawData MUST be multiple of FileAlignment (512)
        0x00, 0x02, 0x00, 0x00, # SizeOfRawData (0x200)
        
        0x00, 0x02, 0x00, 0x00, # PointerToRawData (Offset 512)
        0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00,
        0x00, 0x00,
        0x00, 0x00,
        0x20, 0x00, 0x00, 0x60  # Characteristics (Code | Execute | Read)
    ]
    pe_bytes.extend(section_bytes)

    # Padding Header to 512 bytes
    current_len = len(pe_bytes)
    pe_bytes.extend([0] * (512 - current_len))

    # --- Machine Code (RVA 0x1000 / Offset 512) ---
    # mov eax, 42; ret
    code_bytes = [0xB8, 0x2A, 0x00, 0x00, 0x00, 0xC3]
    pe_bytes.extend(code_bytes)

    # Padding Code Section to 512 bytes (Total File Size = 1024)
    code_padding = 512 - len(code_bytes)
    pe_bytes.extend([0] * code_padding)

    # Emit
    for b in pe_bytes:
        emit_byte(b)

    # Convert to Spaces (Secure Output)
    S, F = " ", "\u3000"
    mapping = {'>':S*3, '<':S*2+F, '+':S+F+S, '-':S+F+F, '.':F+S+S, ',':F+S+F, '[':F*2+S, ']':F*3}
    full_bf = "".join(bf)
    
    # Secure binary write
    output_str = "".join([mapping.get(c, '') for c in full_bf])
    sys.stdout.buffer.write(output_str.encode('utf-8'))

if __name__ == "__main__":
    main()
