#!/usr/bin/env python3
import sys

ptr = 0

def e(s):
    sys.stdout.write(s)

def move_to(target):
    global ptr
    if target > ptr:
        e(">" * (target - ptr))
    if target < ptr:
        e("<" * (ptr - target))
    ptr = target

def set_val(addr, val):
    move_to(addr)
    e("[-]")
    e("+" * val)

def add_val(addr, val):
    move_to(addr)
    e("+" * val)

def sub_val(addr, val):
    move_to(addr)
    e("-" * val)

def copy(src, dst, tmp):
    move_to(tmp)
    e("[-]")
    move_to(dst)
    e("[-]")
    move_to(src)
    e("[")
    move_to(dst)
    e("+")
    move_to(tmp)
    e("+")
    move_to(src)
    e("-")
    e("]")
    move_to(tmp)
    e("[")
    move_to(src)
    e("+")
    move_to(tmp)
    e("-")
    e("]")

def emit_elf_bytes_bf(byte_arr):
    for b in byte_arr:
        move_to(20)
        e("[-]")
        e("+" * b)
        e(".")

